package com.rekahdo.ecommerce.goodshop.utilities;

public class Time {
    public static final long ONE_MILLISECOND = 1000;
    public static final long ONE_SECOND = ONE_MILLISECOND;
    public static final long THIRTY_SECONDS = ONE_SECOND * 30;
    public static final long ONE_MINUTE = THIRTY_SECONDS * 2;
    public static final long ONE_HOUR = ONE_MINUTE * 60;
    public static final long ONE_DAY = ONE_HOUR * 24;
}
